const state = {
  players: "all",
  props: "all",
  search: "",
  sortBy: "none",
  sortDir: "desc"
};

const grid = document.querySelector("#gameGrid");
const resultCount = document.querySelector("#resultCount");
const resultMood = document.querySelector("#resultMood");
const searchInput = document.querySelector("#search");
const pickName = document.querySelector("#pickName");
const sortBySelect = document.querySelector("#sortBy");
const themeToggle = document.querySelector("#themeToggle");
const prefersDark = window.matchMedia("(prefers-color-scheme: dark)");
let themeOverridden = false;

function setTheme(theme) {
  document.documentElement.dataset.theme = theme;
  themeToggle.querySelector("span").textContent = theme === "dark" ? "☀" : "☾";
  themeToggle.setAttribute("aria-label", theme === "dark" ? "Включить светлую тему" : "Включить темную тему");
  themeToggle.title = theme === "dark" ? "Включить светлую тему" : "Включить темную тему";
}

setTheme(prefersDark.matches ? "dark" : "light");

function declension(count) {
  if (count % 10 === 1 && count % 100 !== 11) return "игра";
  if ([2, 3, 4].includes(count % 10) && ![12, 13, 14].includes(count % 100)) return "игры";
  return "игр";
}

function getFilteredGames() {
  const query = state.search.trim().toLowerCase();

  const filtered = games.filter((game) => {
    const matchesPlayers = state.players === "all" || game.group.includes(state.players);
    const matchesProps = state.props === "all" || game.props === state.props;
    const rulesText = game.rules ? [game.rules.goal, game.rules.prep, game.rules.tip, ...game.rules.steps].join(" ") : "";
    const haystack = [game.name, game.description, rulesText, ...game.tags].join(" ").toLowerCase();
    const matchesSearch = !query || haystack.includes(query);
    return matchesPlayers && matchesProps && matchesSearch;
  });

  if (state.sortBy === "none") {
    return filtered;
  }

  return [...filtered].sort((first, second) => {
    const firstValue = first.stats[state.sortBy];
    const secondValue = second.stats[state.sortBy];
    const direction = state.sortDir === "asc" ? 1 : -1;
    return (firstValue - secondValue) * direction || first.name.localeCompare(second.name, "ru");
  });
}

function createMeter(label, value) {
  return `
    <div class="meter">
      <div class="meter-label">
        <span>${label}</span>
        <span>${value}/10</span>
      </div>
      <div class="bar" style="--value: ${value}"><span></span></div>
    </div>
  `;
}

function createRules(game) {
  return `
    <details class="rules">
      <summary>Как играть</summary>
      <div class="rules-body">
        <p><strong>Цель:</strong> ${game.rules.goal}</p>
        <p><strong>Подготовка:</strong> ${game.rules.prep}</p>
        <ol>
          ${game.rules.steps.map((step) => `<li>${step}</li>`).join("")}
        </ol>
        <p class="rule-tip"><strong>Совет:</strong> ${game.rules.tip}</p>
      </div>
    </details>
  `;
}

function renderGames() {
  const filtered = getFilteredGames();
  resultCount.textContent = `${filtered.length} ${declension(filtered.length)}`;
  resultMood.textContent = filtered.length
    ? state.sortBy === "none"
      ? "подходят под настроение"
      : `отсортированы по: ${labels[state.sortBy].toLowerCase()}`
    : "спрятались от фильтров";

  if (!filtered.length) {
    grid.innerHTML = `<div class="empty">Ничего не нашлось. Сбросьте фильтры или придумайте слово попроще, как ведущий в Данетках.</div>`;
    return;
  }

  grid.innerHTML = filtered.map((game) => `
    <article class="game-card" style="--accent: ${game.accent}">
      <div class="game-head">
        <div class="game-icon" aria-hidden="true">${game.icon}</div>
        <div>
          <h3>${game.name}</h3>
          <div class="game-meta">${game.players} игроков · ${game.time}</div>
        </div>
      </div>
      <p class="description">${game.description}</p>
      <div class="tags">
        ${game.tags.map((tag) => `<span class="tag">${tag}</span>`).join("")}
      </div>
      <div class="meters">
        ${Object.entries(game.stats).map(([key, value]) => createMeter(labels[key], value)).join("")}
      </div>
      ${createRules(game)}
    </article>
  `).join("");
}

function pickRandomGame() {
  const filtered = getFilteredGames();
  const source = filtered.length ? filtered : games;
  const game = source[Math.floor(Math.random() * source.length)];
  pickName.textContent = game.name;
  pickName.animate(
    [
      { transform: "scale(0.96)", opacity: 0.65 },
      { transform: "scale(1)", opacity: 1 }
    ],
    { duration: 180, easing: "ease-out" }
  );
}

document.querySelectorAll(".segmented").forEach((group) => {
  group.addEventListener("click", (event) => {
    const button = event.target.closest("button");
    if (!button) return;

    group.querySelectorAll("button").forEach((item) => item.classList.remove("active"));
    button.classList.add("active");
    state[group.dataset.filter] = button.dataset.value;
    renderGames();
  });
});

searchInput.addEventListener("input", (event) => {
  state.search = event.target.value;
  renderGames();
});

sortBySelect.addEventListener("change", (event) => {
  state.sortBy = event.target.value;
  renderGames();
});

document.querySelector("#resetFilters").addEventListener("click", () => {
  state.players = "all";
  state.props = "all";
  state.search = "";
  state.sortBy = "none";
  state.sortDir = "desc";
  searchInput.value = "";
  sortBySelect.value = "none";
  document.querySelectorAll(".segmented").forEach((group) => {
    const defaultValue = group.dataset.default || "all";
    group.querySelectorAll("button").forEach((button) => {
      button.classList.toggle("active", button.dataset.value === defaultValue);
    });
  });
  renderGames();
});

themeToggle.addEventListener("click", () => {
  const currentTheme = document.documentElement.dataset.theme;
  themeOverridden = true;
  setTheme(currentTheme === "dark" ? "light" : "dark");
});

prefersDark.addEventListener("change", (event) => {
  if (themeOverridden) return;
  setTheme(event.matches ? "dark" : "light");
});

document.querySelector("#randomGame").addEventListener("click", pickRandomGame);
document.querySelector("#heroRandom").addEventListener("click", () => {
  document.querySelector("#picker").scrollIntoView({ behavior: "smooth", block: "center" });
  pickRandomGame();
});

